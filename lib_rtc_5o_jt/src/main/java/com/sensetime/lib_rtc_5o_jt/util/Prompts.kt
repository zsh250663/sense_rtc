    fun buildSystemPrompt(
        characterId: String,
        requiredTranslate: Boolean = false,
        requiredTour: Boolean = false,
        requiredStory: Boolean = false,
        requiredMathProblemSolving: Boolean = false,
        requiredEmotion: Boolean = true,
        mathMode: Int = 1
    ): String {
        if (requiredTranslate) {
            return PROMPT_REQUIREMENT_TRANSLATE
        }

        if (requiredTour) {
            return PROMPT_REQUIREMENT_TOUR
        }

        if (requiredStory) {
            return PROMPT_REQUIREMENT_STORY
        }

        if (requiredMathProblemSolving) {
            if (mathMode == 1) {
                return PROMPT_MATH_PROBLEM_SOLVING
            } else if (mathMode == 2) {
                return PROMPT_MATH_PROBLEM_JUDGE
            }
        }

        val promptBuilder = StringBuilder(
            when (characterId) {
//                "zh_female_tianmeixiaoyuan_moon_bigtts" -> PROMPT_CHARACTER_FEMALE_2
//                "zh_female_linjianvhai_moon_bigtts" -> PROMPT_CHARACTER_FEMALE_3
                "nvguo59" -> PROMPT_CHARACTER_FEMALE_1
//                "F12" -> PROMPT_CHARACTER_FEMALE_1
                "M20" -> PROMPT_CHARACTER_MALE_1
//                "zh_male_wennuanahu_moon_bigtts" -> PROMPT_CHARACTER_MALE_3
//                "zh_male_yuanboxiaoshu_moon_bigtts" -> PROMPT_CHARACTER_MALE_2
                "zhili" -> PROMPT_CHARACTER_MALE_1
                else -> PROMPT_CHARACTER_MALE_1
            },
        )

        if (requiredEmotion) {
            promptBuilder.append("\n\n")
            promptBuilder.append(PROMPT_REQUIREMENT_EMOTION)
        }

        return promptBuilder.toString()
    }
